public class Program1{
    public static void main(String[] args) {
        int a = 10;
        int b = a;
        //primitive Type
        System.out.println("The value of a : "+a);
        System.out.println("The value of b : "+b);

        a = 20;

        System.out.println("The value of a : "+a);
        System.out.println("The value of b : "+b);

    }
}
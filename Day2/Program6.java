        int number1 = 5; 
        int number2 = 2;

        //arithmetic operators
        //+ - * / % => modulus operator
        //integer format => %d
        
        //escape sequence
        // \n => new line
                                            //10+20
        System.out.printf("Addition : %d\n", number1 + number2);
